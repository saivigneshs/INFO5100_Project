package Business.Entertainment;

/**
 *
 * @author gowth
 */
public class Musician {
    private String musicianno;
    private String MusicPlan;
    private String cost;
    private String numofpeople;
    private String Descr;

    public String getMusicianno() {
        return musicianno;
    }

    public void setMusicianno(String musicianno) {
        this.musicianno = musicianno;
    }

    public String getMusicPlan() {
        return MusicPlan;
    }

    public void setMusicPlan(String MusicPlan) {
        this.MusicPlan = MusicPlan;
    }

    public String getCost() {
        return cost;
    }

    public void setCost(String cost) {
        this.cost = cost;
    }

    public String getNumofpeople() {
        return numofpeople;
    }

    public void setNumofpeople(String numofpeople) {
        this.numofpeople = numofpeople;
    }

    public String getDescr() {
        return Descr;
    }

    public void setDescr(String Descr) {
        this.Descr = Descr;
    }

    
}
